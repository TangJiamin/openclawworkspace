#!/bin/bash

# storyboard-generator 调用脚本
# 将剧本转换为分镜表和静帧提示词

# 使用方法
usage() {
    echo "分镜表生成器"
    echo ""
    echo "使用方法:"
    echo "  $0 <script_file> <output_file>"
    echo ""
    echo "参数:"
    echo "  script_file : 剧本文件路径"
    echo "  output_file : 分镜表输出文件路径"
    echo ""
    echo "示例:"
    echo "  $0 \"script.txt\" \"storyboard.txt\""
    exit 1
}

if [ $# -lt 2 ]; then
    usage
fi

SCRIPT_FILE="$1"
OUTPUT_FILE="$2"

echo "🎬 分镜表生成器"
echo "========================================"
echo ""
echo "📄 剧本文件: $SCRIPT_FILE"
echo "📝 输出文件: $OUTPUT_FILE"
echo ""

# 检查文件是否存在
if [ ! -f "$SCRIPT_FILE" ]; then
    echo "❌ 错误: 剧本文件不存在"
    exit 1
fi

# 输出分镜表格式
cat > "$OUTPUT_FILE" << 'EOF'
## 本集导演锚点

- **本集目标：** (一句话目标)
- **主情绪：** (紧张/热血/温馨/压抑)
- **钩子：** (前10秒抓手)
- **场景列表：**
  - 1-1 (场景名)
  - 1-2 (场景名)
- **主要角色：** (角色列表)

---

## 导演三帧分镜法

### 场次：1-1 场景名

#### 起帧 (KF_A)
- **镜头ID：** E01_S01_KF_A
- **目的标签：** 信息/情绪/动作/反转/钩子（选1）
- **唯一信息点：** (一句话)
- **定格瞬间：** (一句话，可截图)
- **景别/机位：** (全景/中景/近景/特写) + (平视/俯视/仰视/主观)
- **主导运镜：** 静止/微推/拉远/轻摇/跟拍（选1）

**提示词（三段式）：**
- 风格： (STYLE_BASE一句话)
- 主体： (@角色ID形象描述)，(本镜头表情动作)
- 画面： (SceneKey场景描述)，(景别)，(机位)，(构图)，(定格瞬间)

#### 爆帧 (KF_B)
- **镜头ID：** E01_S02_KF_B
- **目的标签：** 信息/情绪/动作/反转/钩子（选1）
- **唯一信息点：** (一句话)
- **定格瞬间：** (一句话，可截图)
- **景别/机位：** (全景/中景/近景/特写) + (平视/俯视/仰视/主观)
- **主导运镜：** 静止/微推/拉远/轻摇/跟拍（选1）

**提示词（三段式）：**
- 风格： (STYLE_BASE一句话)
- 主体： (@角色ID形象描述)，(本镜头表情动作)
- 画面： (SceneKey场景描述)，(景别)，(机位)，(构图)，(定格瞬间)

#### 钩帧 (KF_C)
- **镜头ID：** E01_S03_KF_C
- **目的标签：** 信息/情绪/动作/反转/钩子（选1）
- **唯一信息点：** (一句话)
- **定格瞬间：** (一句话，可截图)
- **景别/机位：** (全景/中景/近景/特写) + (平视/俯视/仰视/主观)
- **主导运镜：** 静止/微推/拉远/轻摇/跟拍（选1）

**提示词（三段式）：**
- 风格： (STYLE_BASE一句话)
- 主体： (@角色ID形象描述)，(本镜头表情动作)
- 画面： (SceneKey场景描述)，(景别)，(机位)，(构图)，(定格瞬间)

---

## 镜头表（Shot List）

| ShotID | SceneID | 景别 | 机位 | 运镜 | 目的 | 唯一信息点 | 定格瞬间 | 动作链 | 台词/OS | CharID | SceneKey | 优先级 | 状态 |
|--------|---------|------|------|------|------|------------|----------|--------|---------|--------|----------|--------|------|
| E01_S01 | 1-1 | 全景 | 平视 | 静止 | 信息 | (信息点) | (定格) | (动作) | (台词) | @xxx | scene_xxx | P0 | todo |
| E01_S02 | 1-1 | 近景 | 平视 | 微推 | 情绪 | (信息点) | (定格) | (动作) | (台词) | @xxx | scene_xxx | P1 | todo |

---

## 静帧提示词清单

### E01_S01

**三段式提示词：**
- 风格： (STYLE_BASE一句话)
- 主体： (角色形象)，(表情动作)
- 画面： (场景描述)，(景别)(机位)，(构图描述)，(主体位置)，(定格瞬间)

**完整提示词（直接复制用）：**
```
(STYLE_BASE)，(角色形象)，(表情动作)，(场景描述)，(景别)(机位)，(构图)，(定格瞬间)，高清细节
```

---

## QC 检查清单

### P0（必须修复 - 影响剪辑）
- [ ] 是否缺建立/反应/转场镜头？
- [ ] 定格瞬间是否"一眼看懂"？
- [ ] 是否有轴线跳跃？
- [ ] 信息点是否清晰？

### P1（第二优先 - 影响一致性）
- [ ] 角色脸/服装是否漂移？
- [ ] 道具是否与角色卡一致？
- [ ] 场景锚点是否保持？

### P2（最后修复 - 美观问题）
- [ ] 画面是否足够干净？
- [ ] 光影是否统一？
- [ ] 是否足够电影化？

EOF

echo "✅ 分镜表文件已创建: $OUTPUT_FILE"
echo ""
echo "📋 下一步:"
echo "1. 完善本集导演锚点（目标、情绪、钩子）"
echo "2. 为每场戏规划三帧分镜（起帧、爆帧、钩帧）"
echo "3. 展开完整镜头表"
echo "4. 为每个镜头生成三段式提示词"
echo "5. 执行 QC 检查清单"
echo ""
echo "========================================"
echo "✅ 完成"
